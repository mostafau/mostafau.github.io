<b>MagnoTricorder:</b> Smart Home is becoming a hot area of research for both academic and industrial researchers. In Smart Home, sensing the status of home devices (e.g., home appliances) is a corner stone for having better control over the home appliances as well as power consumption. In this project we present the design and the evaluation of a framework MagnoTricorder, a system that utilizes the magnetic sensor in smartphones to detect the running devices at home thru a singlepoint sensing. MagnoTricorder leverages the effect of Electro Magnetic Interference (EMI) generated by the AC current in the main power-line at home. This EMI induces a magnetic ﬁeld that highly ﬂuctuates the reading of the magnetic sensor in smartphones. In this project, we utilize this characteristic for detecting and identifying the running devices at home thru the Circuit Breaker Panel.
