<b>EnergySniffer:</b> In this project, we propose a simple and flexible energy monitoring system using smart phones. We call our system EnergySniffer in which it exploits various sensors, such as magnetic sensor, light, microphone, temperature, camera, WiFi, in smart phones to detect and monitor operating machines in its vicinity. The advantages of EnergySniffer system can be summarized as follow: First, it monitors energy consumption for each individual machine. Second, it has very low overhead and also no new hardware is needed to install or maintain. Third, very flexible in updating software and deploying new services using the application updating feature of the smart phones' application markets. Using the sensors in smart phones to monitor the energy consumption by machines is an eccentric way to approach the problem. Our final objective is to fuse the data from multiple sensors in phone to build a multi sensing framework to generate a unique fingerprint profile for each machine. Later, we apply a machine learning method using fingerprint profiles to recognize and monitor operating machines. In addition to that, this system will also communicate with the Energy Profile of the identiﬁed machine to finally calculate the actual energy consumption.
